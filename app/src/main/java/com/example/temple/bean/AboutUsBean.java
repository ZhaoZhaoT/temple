package com.example.temple.bean;

public class AboutUsBean {
    private String company_name;
    private String company_phone;
    private String work_day;
    private String company_email;
    private String user_agreement;
    private String private_sign;

    public String getCompany_name() {
        return company_name;
    }

    public void setCompany_name(String company_name) {
        this.company_name = company_name;
    }

    public String getCompany_phone() {
        return company_phone;
    }

    public void setCompany_phone(String company_phone) {
        this.company_phone = company_phone;
    }

    public String getWork_day() {
        return work_day;
    }

    public void setWork_day(String work_day) {
        this.work_day = work_day;
    }

    public String getCompany_email() {
        return company_email;
    }

    public void setCompany_email(String company_email) {
        this.company_email = company_email;
    }

    public String getUser_agreement() {
        return user_agreement;
    }

    public void setUser_agreement(String user_agreement) {
        this.user_agreement = user_agreement;
    }

    public String getPrivate_sign() {
        return private_sign;
    }

    public void setPrivate_sign(String private_sign) {
        this.private_sign = private_sign;
    }
}
